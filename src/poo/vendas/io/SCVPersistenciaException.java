package poo.vendas.io;

public class SCVPersistenciaException extends Exception {
	private static final long serialVersionUID = 1L;

	public SCVPersistenciaException() {
	}

	public SCVPersistenciaException(String arg0) {
		super(arg0);
	}

	public SCVPersistenciaException(Throwable arg0) {
		super(arg0);
	}

	public SCVPersistenciaException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}

